package com.library.model;

public class DelMyReo {
    String uno;
    String btxm;

    public DelMyReo() {
    }

    public DelMyReo(String uno, String btxm) {
        this.uno = uno;
        this.btxm = btxm;
    }

    public String getUno() {
        return uno;
    }

    public void setUno(String uno) {
        this.uno = uno;
    }

    public String getBtxm() {
        return btxm;
    }

    public void setBtxm(String btxm) {
        this.btxm = btxm;
    }
}
